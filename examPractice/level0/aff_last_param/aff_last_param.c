#include <unistd.h>

int main (int ac, char ** av)
{
	int		i;
	char	temp;

	i = 0;
	if (ac >= 2)
	{
		while (av[ac - 1][i] != '\0' )
		{
			temp = av[ac - 1][i];
			write (0, &temp, 1);
			i++;
		}
	}
	write (1, "\n", 1);
	return (0);
}
