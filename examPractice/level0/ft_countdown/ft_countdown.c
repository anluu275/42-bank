#include <unistd.h>

int main ()
{
	char c;

	c = '9';

	while (c > '0')
	{
		write (1, &c, 1);
		c--;
	}
	write (1, &c,1);
	write ( 1, "\n", 1);

	//write (1, "9876543210", 10);
	return (0);
}
