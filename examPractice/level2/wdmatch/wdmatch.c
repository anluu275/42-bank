#include <unistd.h>

int main (int ac, char **av)
{
	int		x;
	int		y;

	x = 0;
	y = 0;
	if (ac == 3)
	{
		while (av[1][x] != '\0' && av[2][y] != '\0')
		{
			if (av[1][x] == av[2][y])
				x++;
			y++;
		}
		if (av[1][x] == '\0')
		{
			y = 0;
			while (av[1][y] != '\0')
				write (1,&av[1][y++], 1);
		}
	}
	write (1, "\n", 1);
	return (0);
}
