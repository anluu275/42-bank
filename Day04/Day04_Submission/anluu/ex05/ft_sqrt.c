int	ft_sqrt(int nb)
{
	int		counter;
	int		minus;

	counter = 0;
	minus = 1;
	while (nb >= 0)
	{
		nb = nb - minus;
		minus += 2;
		counter++;
		if (nb == 0)
		{
			return (counter);
		}
	}
	return (0);
}
