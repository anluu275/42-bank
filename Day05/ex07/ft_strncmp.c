int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int		i;

	i = 0;
	while (*s1 && *s2 && n > 0)
	{
		if (*s1 - *s2 != 0)
		{
			i = (*s1 - *s2);
			return (i);
		}
		s1++;
		s2++;
		n--;
	}
	return (i);
}
